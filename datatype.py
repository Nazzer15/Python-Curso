# Tipos de datos en python
# Strings 
nombre = "Jose David"
print(nombre)

# Numbers
# Integer 
print(15)

# Float
print(30.3)

# Boolean
# Iniciando con mayusculas
print(False)
print(True)

# List - Arrays
# Los datos se pueden cambiar
[10, 20, 30]
Hello = ['Hello', 'World']
print(Hello)

# Tuples
# Las Tuples no pueden cambiarse
(10, 20, 30, 55)

# Dictorionies
myInfo = {
    "name": "Jose David",
    "lastname": "Jimenez Badilla",
    "nickname": "Nazzer"
}

print(myInfo)

# Imprime el tipo de dato
print(type({
    "color": "red",
    "type": "Toyota"
}))
#{ "key": "value" }

# Tipo de dato no definido - Default
None
print(None)


